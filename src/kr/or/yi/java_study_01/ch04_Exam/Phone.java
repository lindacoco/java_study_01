package kr.or.yi.java_study_01.ch04_Exam;

class Phone {

	public Phone(String name, int tel) {
		this.name = name;
		this.tel = tel;
	}



	private String name;
	private int tel;
	
	
	
	public Phone() {	
		
	}



	public Phone(String name) {
		this.name = name;
	}



	public Phone(int tel) {
		this.tel = tel;
	}
       
    
}


